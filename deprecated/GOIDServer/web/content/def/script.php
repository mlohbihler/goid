A script is a program written in ECMAScript (aka Javascript, see ecmascript.org for more details). Although this is
the same language used for scripting in browsers, there are important contextual differences. For example, there is no
"document" object. Instead there are "senses", "actuators", "console" (for writing messages to the output panel), and
"Utils" (a collection of useful functions).<br/>
<br/>
Typically, a script will use the senses as input, and then write output values to the actuators. The script can also
maintain memory (in the form or global variables), use execution control structures, define function and objects, etc.
All of the usual stuff.